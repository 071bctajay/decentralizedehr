$(function(){
    $('#checkicon1').hide();
    $('#finishDr').hide();
    $('#finishPatient').hide();
    $('#firstform-post').on('click',function(event){
        $('#firstform-post').hide();
        $('#checkicon1').show();
        $('#finishPatient').hide();
    });

    $('#checkicon2').hide();
    $('#secondform-post').on('click',function(event){
        $('#secondform-post').hide();
        $('#checkicon2').show();
    });    
    $('#thirdform-postpatient').on('click',function(event){
        $('#thirdform-postpatient').hide();
        $('#finishPatient').show();
    });    
});
